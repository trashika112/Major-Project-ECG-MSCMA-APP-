def run_epoch(loader, train=True, mixup_alpha=0.0):
    model.train(train)
    total_loss = 0.0
    all_logits, all_targets = [], []

    for xb, yb in tqdm(loader, leave=False):
        xb, yb = xb.to(DEVICE, non_blocking=True), yb.to(DEVICE, non_blocking=True)

        if train:
            xb_in, yb_in = mixup_batch(xb, yb, mixup_alpha) if mixup_alpha > 0 else (xb, yb)
            optimizer.zero_grad(set_to_none=True)
            with torch.amp.autocast('cuda', enabled=(DEVICE.type == 'cuda')):
                logits = model(xb_in)
                loss = criterion(logits, yb_in)
            scaler.scale(loss).backward()
            scaler.unscale_(optimizer)
            torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            scaler.step(optimizer)
            scaler.update()
        else:
            with torch.no_grad(), torch.amp.autocast('cuda', enabled=(DEVICE.type == 'cuda')):
                logits = model(xb)
                loss = criterion(logits, yb)

        total_loss += loss.item() * xb.size(0)
        all_logits.append(logits.detach().float().cpu())
        all_targets.append(yb.detach().float().cpu())  # log true targets even under mixup (rough train-time signal only)

    all_logits = torch.cat(all_logits).numpy()
    all_targets = torch.cat(all_targets).numpy()
    probs = 1 / (1 + np.exp(-all_logits))

    try:
        macro_auc = roc_auc_score(all_targets, probs, average='macro')
    except ValueError:
        macro_auc = float('nan')  # can happen if a class has no positives in a small batch subset

    f1 = f1_score(all_targets, (probs >= 0.5).astype(int), average='macro', zero_division=0)
    return total_loss / len(loader.dataset), macro_auc, f1, probs, all_targets

