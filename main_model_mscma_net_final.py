"""## Change 1 — Train-time data augmentation
Adds jitter, per-lead amplitude scaling, small time shift, and occasional single-lead dropout **only to the training split**. This is standard regularization for small-to-mid sized ECG datasets and should reduce overfitting (your original run showed train_loss dropping to 0.22 while val_loss rose back to 0.71-0.94 — a classic overfitting signature).
"""

class PTBXLDataset(Dataset):
    """
    Same as before, but the TRAIN split now gets light, physiologically
    plausible augmentation applied on-the-fly (val/test stay untouched
    so evaluation numbers remain a fair, deterministic readout).

    Why these four and not others: they mimic real acquisition variance
    (electrode noise, gain differences, patient positioning / R-peak
    phase, occasional bad lead) without destroying the diagnostic
    morphology of the ECG (we do NOT time-reverse or lead-shuffle).
    """
    def __init__(self, X, y, train=False, aug_prob=0.5):
        # X: (N, T, 12) -> we store as (N, 12, T) for Conv1d
        self.X = torch.from_numpy(X).float().permute(0, 2, 1).contiguous()
        self.y = torch.from_numpy(y).float()
        self.train = train
        self.aug_prob = aug_prob

    def __len__(self):
        return len(self.X)

    def _augment(self, x):
        # x: (12, T)
        if random.random() < self.aug_prob:
            # additive Gaussian noise -> simulates muscle/mains noise
            x = x + torch.randn_like(x) * random.uniform(0.0, 0.05)
        if random.random() < self.aug_prob:
            # per-lead amplitude scaling -> simulates electrode/gain variance
            scale = 1.0 + (torch.rand(x.shape[0], 1) - 0.5) * 0.2   # +/-10%
            x = x * scale
        if random.random() < self.aug_prob:
            # small circular time shift -> simulates R-peak phase variability
            shift = random.randint(-50, 50)
            x = torch.roll(x, shifts=shift, dims=-1)
        if random.random() < 0.2:
            # random single-lead dropout -> simulates a noisy/disconnected lead
            x = x.clone()
            lead = random.randint(0, x.shape[0] - 1)
            x[lead] = 0.0
        return x

    def __getitem__(self, idx):
        x = self.X[idx]
        if self.train:
            x = self._augment(x)
        return x, self.y[idx]

