import types
import numpy as np
import torch
import torch.nn.functional as F
import matplotlib.pyplot as plt

# ---- 1. Monkey-patch AttentionBlock.forward to also return its weights ----
# (the model's own forward() calls need_weights=False for speed during
#  normal training/inference -- we only turn it on for this diagnostic pass)
_captured = {}

def _attn_forward_capture(self, x):
    h = self.norm1(x)
    attn_out, attn_w = self.mha(h, h, h, need_weights=True, average_attn_weights=False)
    # attn_w: (B, num_heads, L, L) -- query x key attention per head
    _captured['weights'] = attn_w.detach()
    x = x + attn_out
    x = x + self.ff(self.norm2(x))
    return x

_original_attn_forward = model.attn.forward   # save so we can restore it afterward
model.attn.forward = types.MethodType(_attn_forward_capture, model.attn)

# ---- 2. Rebuild the same single-label, correctly-classified example set ----
single_label_mask = test_targets.sum(axis=1) == 1
y_true_single = np.argmax(test_targets[single_label_mask], axis=1)
y_pred_single = np.argmax(test_probs[single_label_mask], axis=1)
conf_single = test_probs[single_label_mask].max(axis=1)

all_test_x = []
for xb, _ in test_loader:
    all_test_x.append(xb)
X_test_tensor = torch.cat(all_test_x, dim=0)

T_RAW = X_test_tensor.shape[-1]      # 1000
T_ATTN = T_RAW // 8                  # 125, after 3x maxpool(2)

fig, axes = plt.subplots(len(CLASSES), 1, figsize=(11, 2.2 * len(CLASSES)), sharex=True)

for i, cls in enumerate(CLASSES):
    idxs = np.where((y_true_single == i) & (y_pred_single == i))[0]
    if len(idxs) == 0:
        axes[i].set_title(f"{cls}: no correctly-classified single-label example found")
        continue
    best_idx = idxs[np.argmax(conf_single[idxs])]

    x = X_test_tensor[single_label_mask][best_idx:best_idx + 1].to(DEVICE)

    model.eval()
    with torch.no_grad():
        _ = model(x)   # triggers the patched forward, fills _captured['weights']

    attn_w = _captured['weights'][0]                 # (num_heads, L, L)
    attn_w = attn_w.mean(dim=0)                       # average over heads -> (L, L)
    received = attn_w.mean(dim=0)                     # avg attention each position RECEIVES -> (L,)
    received = received.cpu().numpy()
    received = (received - received.min()) / (received.max() - received.min() + 1e-8)

    # upsample from T_ATTN (125) back to T_RAW (1000) -- matches the 3x maxpool(2) downsampling
    received_t = torch.tensor(received)[None, None, :]
    received_up = F.interpolate(received_t, size=T_RAW, mode='linear', align_corners=False)
    heatmap = received_up.squeeze().numpy()

    lead_ii = x[0, 1].detach().cpu().numpy()

    ax = axes[i]
    ax.plot(lead_ii, color='black', linewidth=0.9)
