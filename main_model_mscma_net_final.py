def tta_forward(m, xb):
    """Average logits over the clean signal + two light, physiologically
    plausible perturbations (small time shift, small amplitude scaling).
    This is inference-time-only and does not touch the true labels."""
    with torch.no_grad(), torch.amp.autocast('cuda', enabled=(DEVICE.type == 'cuda')):
        preds = [m(xb), m(torch.roll(xb, shifts=10, dims=-1)), m(xb * 1.05)]
    return torch.stack(preds, dim=0).mean(dim=0)

def get_probs(loader, use_tta=True):
    all_logits, all_targets = [], []
    with torch.no_grad():
        for xb, yb in loader:
            xb = xb.to(DEVICE)
            per_model_logits = []
            for m in models_for_ensemble:
                if use_tta:
                    per_model_logits.append(tta_forward(m, xb))
                else:
                    with torch.amp.autocast('cuda', enabled=(DEVICE.type == 'cuda')):
                        per_model_logits.append(m(xb))
            logits = torch.stack(per_model_logits, dim=0).mean(dim=0)
            all_logits.append(logits.float().cpu())
            all_targets.append(yb.float())
    logits = torch.cat(all_logits).numpy()
    targets = torch.cat(all_targets).numpy()
    probs = 1 / (1 + np.exp(-logits))
    return probs, targets

