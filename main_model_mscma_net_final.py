class FocalBCELoss(nn.Module):
    """
    BCEWithLogits + per-class pos_weight (for the NORM/HYP imbalance you already
    had) + a focal modulating term (gamma) that down-weights *easy* examples the
    model already gets right, so gradient signal concentrates on the hard cases
    (this is exactly where HYP -- your weakest class at AUC 0.905 -- needs help).
    A small amount of label smoothing is also applied to soften the hard 0/1
    targets, which tends to reduce over-confident, poorly-calibrated logits.
    """
    def __init__(self, pos_weight, gamma=1.5, label_smoothing=0.02):
        super().__init__()
        self.register_buffer('pos_weight', pos_weight)
        self.gamma = gamma
        self.eps = label_smoothing

    def forward(self, logits, targets):
        targets = targets * (1 - self.eps) + 0.5 * self.eps
        p = torch.sigmoid(logits)
        pt = torch.where(targets >= 0.5, p, 1 - p)
        focal_weight = (1 - pt).clamp(min=1e-4) ** self.gamma
        bce = F.binary_cross_entropy_with_logits(
            logits, targets, pos_weight=self.pos_weight, reduction='none')
        return (focal_weight * bce).mean()


def mixup_batch(x, y, alpha=0.2):
    """
    Mixup adapted for multi-label targets: linearly interpolate both the raw
    signal and the multi-hot label vector between two random training examples.
    Acts as a strong, well-established regularizer -- set MIXUP_ALPHA = 0 below
    to disable it if you want an ablation run without it.
    """
    if alpha <= 0:
        return x, y
    lam = float(np.random.beta(alpha, alpha))
    idx = torch.randperm(x.size(0), device=x.device)
    x_mixed = lam * x + (1 - lam) * x[idx]
    y_mixed = lam * y + (1 - lam) * y[idx]
    return x_mixed, y_mixed

